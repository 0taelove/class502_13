package exam04;

import static java.lang.Math.*;

public class Ex01 {
    public static void main(String[] args) {
        System.out.println(abs(-10)); // 10 절대 값
        System.out.println(ceil(10.2)); // 11 올림
        System.out.println(floor(10.2)); // 10 버림
        System.out.println(round(10.5)); // 11 반 올림
        System.out.println(pow(2, 4)); // 16 2의 4승
        System.out.println(sqrt(9)); // 3
    }
}
