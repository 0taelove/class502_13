import java.lang.*; // 자동 추가

// 모든 class는 Object의 하위 class 이다 -> Object에 정의 되어있는 객체의 기능 모두 사용 가능
public class Ex01 extends java.lang.Object { // extends java.lang.Object 자동 추가
    public static void main(String[] args) {
        java.lang.String str = "ABC"; // String class 에서는 java.lang 자동 추가
    }
}
